#include <iostream>
using namespace std;

int main() {
    int a = 8, b = 3;

    // Addition
    cout << "a + b = " << (a + b) << endl;
  
    // Subtraction
    cout << "a - b = " << (a - b) << endl;
  
    // Multiplication
    cout << "a * b = " << (a * b) << endl;
  
    // Division
    cout << "a / b = " << (a / b) << endl;
  
    // Modulo
    cout << "a % b = " << (a % b) << endl;


    cout <<endl;
  
    // Increament
    //1.PRE INCREMENT
    cout << "++a = " << ++a << endl;

    //2.POST INCREMENT
    cout << "a++ = " << a++ << endl;

    cout <<endl;

    // Decrement
    //1.PRE DECREMENT
     cout << "--b = " << --b << endl;

     //2.POST DECREMENT
     cout << "b-- =" << b-- << endl;

  
        return 0;
}